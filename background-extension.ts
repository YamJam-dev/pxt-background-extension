    //% block="sprite background $Img scale $Scale"
    //% Scale.defl=1
    //% group="background"
    //% Img.shadow=screen_image_picker
    export function spriteBackground(Img: Image, Scale: number) {
        backgroundImage = sprites.create(Img, SpriteKind.background)
        backgroundImage.setScale(Scale, ScaleAnchor.Middle)
        backgroundImage.setFlag(SpriteFlag.RelativeToCamera, true)
        backgroundImage.setPosition(scene.cameraProperty(CameraProperty.X), scene.cameraProperty(CameraProperty.Y))
        backgroundImage.z = 1 / 0 * -1

    }

    //% block="background animation frames $frames interval $interval scale $scale"
    //% interval.defl=100
    //% scale.defl=1
    //% group="background"
    //% frames.shadow=animation_editor
    export function backgroundAnimation(frames: Image[], interval: number, scale: number) {
        backgroundAnim = sprites.create(frames[0], SpriteKind.backAnim)
        backgroundAnim.z = 1 / 0 * -1
        backgroundAnim.setFlag(SpriteFlag.RelativeToCamera, true)
        backgroundAnim.scale = scale
        backgroundAnim.setPosition(scene.cameraProperty(CameraProperty.X), scene.cameraProperty(CameraProperty.Y))
        animation.runImageAnimation(
            backgroundAnim,
            frames,
            interval,
            true
        );
    }

